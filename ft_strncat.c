#include "libft.h"

char	ft_strncat(char *s1, char *s2, int n)
{
	char *temp;

	temp = s1;
	while (*s1 != '\0')
		s1++;
	while (*s2 != '\0' && n-- > 0)
		*s1++ = *s2++;
	*s1 = '\0';
	return (temp);
}
